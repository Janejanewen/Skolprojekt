CREATE TABLE IF NOT EXISTS "player" (
	"id"	INTEGER,
	"name"	TEXT,
	PRIMARY KEY("id" AUTOINCREMENT)
);
CREATE TABLE IF NOT EXISTS "fan" (
	"id"	INTEGER,
	"name"	TEXT,
	PRIMARY KEY("id" AUTOINCREMENT)
);
CREATE TABLE IF NOT EXISTS "fan_email" (
	"fan_id"	INTEGER,
	"email"	TEXT UNIQUE,
	FOREIGN KEY("fan_id") REFERENCES "fan"("id") ON UPDATE CASCADE
);
CREATE TABLE IF NOT EXISTS "match" (
	"id"	INTEGER,
	"location"	TEXT,
	"date"	TEXT,
	PRIMARY KEY("id" AUTOINCREMENT)
);
CREATE TABLE IF NOT EXISTS "team" (
	"id"	INTEGER,
	"name"	TEXT,
	PRIMARY KEY("id" AUTOINCREMENT)
);
CREATE TABLE IF NOT EXISTS "current_team" (
	"team_id"	INTEGER,
	"player_id"	INTEGER,
	FOREIGN KEY("team_id") REFERENCES "team"("id") ON UPDATE CASCADE,
	FOREIGN KEY("player_id") REFERENCES "player"("id") ON UPDATE CASCADE
);
CREATE TABLE IF NOT EXISTS "fan_player" (
	"player_id"	INTEGER,
	"fan_id"	INTEGER,
	FOREIGN KEY("player_id") REFERENCES "player"("id") ON UPDATE CASCADE,
	FOREIGN KEY("fan_id") REFERENCES "fan"("id") ON UPDATE CASCADE
);
CREATE TABLE IF NOT EXISTS "fan_team" (
	"team_id"	INTEGER,
	"fan_id"	INTEGER,
	FOREIGN KEY("team_id") REFERENCES "team"("id") ON UPDATE CASCADE,
	FOREIGN KEY("fan_id") REFERENCES "fan"("id") ON UPDATE CASCADE
);
CREATE TABLE IF NOT EXISTS "result" (
	"match_id"	INTEGER,
	"throw_player_id"	INTEGER,
	"hit_player_id"	INTEGER,
	FOREIGN KEY("match_id") REFERENCES "match"("id"),
	FOREIGN KEY("throw_player_id") REFERENCES "player"("id")
);
CREATE TABLE IF NOT EXISTS "team_set" (
	"set_nr"	INTEGER,
	"spot"	INTEGER,
	"player_id"	INTEGER,
	"match_id"	INTEGER,
	"team_id"	INTEGER,
	PRIMARY KEY("set_nr","spot"),
	FOREIGN KEY("player_id") REFERENCES "player"("id") ON UPDATE CASCADE,
	FOREIGN KEY("match_id") REFERENCES "match"("id")
);
